// ─── Helpers fichiers ────────────────────────────────────────────────

function csv(lignes: string[]): { buffer: Buffer; originalname: string; size: number } {
  const text = lignes.join('\n') + '\n';
  const buffer = Buffer.from(text, 'utf-8');
  return { buffer, originalname: 'budget.csv', size: buffer.length };
}

const HEADER =
  'code_cr,code_compte,code_ligne_metier,mois,mode_saisie,montant,encours_moyen,tie,commentaire';

async function xlsx(rows: string[][]): Promise<{
  buffer: Buffer;
  originalname: string;
  size: number;
}> {
  const wb = new ExcelJS.Workbook();
  const ws = wb.addWorksheet('Budget');
  for (const r of rows) ws.addRow(r);
  const buf = (await wb.xlsx.writeBuffer()) as ArrayBuffer;
  const buffer = Buffer.from(new Uint8Array(buf));
  return { buffer, originalname: 'budget.xlsx', size: buffer.length };
}

