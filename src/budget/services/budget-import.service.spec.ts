// ─── DB setup ────────────────────────────────────────────────────────

function buildMemDb(): IMemoryDb {
  const db = newDb({ autoCreateForeignKeyIndices: true });
  db.public.registerFunction({
    name: 'current_database',
    args: [],
    returns: DataType.text,
    implementation: () => 'test',
  });
  db.public.registerFunction({
    name: 'version',
    args: [],
    returns: DataType.text,
    implementation: () => 'PostgreSQL 15 (pg-mem)',
  });
  return db;
}

async function createDataSource(): Promise<DataSource> {
  const db = buildMemDb();
  const ds: DataSource = db.adapters.createTypeormDataSource({
    type: 'postgres',
    entities: [AuditLog],
    synchronize: true,
  });
  await ds.initialize();

  // Tables minimales — uniquement les colonnes consommées par le service.
  await ds.query(`
    CREATE TABLE dim_devise (
      id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      code_iso varchar(3) NOT NULL
    )`);
  await ds.query(`
    CREATE TABLE dim_produit (
      id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      code_produit varchar(50) NOT NULL,
      version_courante boolean DEFAULT true,
      est_actif boolean DEFAULT true,
      niveau int DEFAULT 1
    )`);
  await ds.query(`
    CREATE TABLE dim_segment (
      id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      code_segment varchar(50) NOT NULL,
      version_courante boolean DEFAULT true,
      est_actif boolean DEFAULT true
    )`);
  await ds.query(`
    CREATE TABLE dim_centre_responsabilite (
      id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      code_cr varchar(50) NOT NULL,
      libelle varchar(200) NOT NULL,
      fk_structure bigint NOT NULL,
      version_courante boolean DEFAULT true,
      est_actif boolean DEFAULT true
    )`);
  await ds.query(`
    CREATE TABLE dim_compte (
      id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      code_compte varchar(20) NOT NULL,
      classe varchar(50) NOT NULL,
      est_compte_collectif boolean DEFAULT false,
      version_courante boolean DEFAULT true
    )`);
  await ds.query(`
    CREATE TABLE dim_ligne_metier (
      id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      code_ligne_metier varchar(50) NOT NULL,
      version_courante boolean DEFAULT true
    )`);
  await ds.query(`
    CREATE TABLE dim_temps (
      id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      date date NOT NULL UNIQUE,
      jour int NOT NULL,
      mois int NOT NULL,
      annee int NOT NULL
    )`);
  await ds.query(`
    CREATE TABLE dim_version (
      id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      code_version varchar(50) NOT NULL,
      libelle varchar(200) NOT NULL,
      statut varchar(20) NOT NULL DEFAULT 'ouvert'
    )`);
  await ds.query(`
    CREATE TABLE dim_scenario (
      id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      code_scenario varchar(50) NOT NULL
    )`);
  await ds.query(`
    CREATE TABLE fait_budget (
      id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      fk_temps bigint NOT NULL,
      fk_compte bigint NOT NULL,
      fk_structure bigint NOT NULL,
      fk_centre bigint NOT NULL,
      fk_ligne_metier bigint NOT NULL,
      fk_produit bigint NOT NULL,
      fk_segment bigint NOT NULL,
      fk_devise bigint NOT NULL,
      fk_version bigint NOT NULL,
      fk_scenario bigint NOT NULL,
      montant_devise numeric(20,2) NOT NULL,
      montant_fcfa numeric(20,2) NOT NULL,
      taux_change_applique numeric(8,5) NOT NULL,
      mode_saisie varchar(20) NOT NULL DEFAULT 'MONTANT',
      encours_moyen numeric(20,2) NULL,
      tie numeric(8,5) NULL,
      commentaire text NULL,
      utilisateur_creation varchar(255) NOT NULL,
      date_modification timestamp NULL,
      utilisateur_modification varchar(255) NULL,
      CONSTRAINT uq_fait_budget_grain UNIQUE
        (fk_temps, fk_compte, fk_structure, fk_centre, fk_ligne_metier,
         fk_produit, fk_segment, fk_devise, fk_version, fk_scenario)
    )`);

  return ds;
}

